<script>
    var imgCount = 5;
    var index = 1;
    var index2 = 1;
    var intervalId;
    var buttonSpan = $('.top1_ul_first')[0].children;
    var buttonSpan2 = $('#top1_ul_second')[0].children;
    //自动轮播功能 使用定时器
    autoNextPage();
    function autoNextPage(){
        intervalId = setInterval(function(){
            nextPage(true);
            nextPage2(true);
        },3000);
    }
    //当鼠标移入 停止轮播
    $('.container').mouseover(function(){
        console.log('hah');
        clearInterval(intervalId);
    });
    // 当鼠标移出，开始轮播
    $('.container').mouseout(function(){
        autoNextPage();
    });

    clickButtons();
    function clickButtons(){
        var length = buttonSpan.length;
        var length2 = buttonSpan2.length;
        for(var i=0;i<length;i++){
            buttonSpan[i].onclick = function(){
                if($(this).attr('index')==1){
                    index = 5;
                }else{
                    index = $(this).attr('index')-1;
                }
                nextPage(true);

            };
        }
        for(var i=0;i<length2;i++){
            buttonSpan2[i].onclick = function(){
                if($(this).attr('index')==1){
                    index2= 5;
                }else{
                    index2 = $(this).attr('index')-1;
                }
                nextPage2(true);

            };
        }
    }
    function nextPage(next){
        var targetLeft = 0;

        if(next){//往后走
            if(index == 5){//到最后一张，直接跳到第一张
                targetLeft = 0;
                index = 1;
            }else{
                index++;
                targetLeft = -1000*(index-1);
            }

        }else{//往前走
            if(index == 1){//在第一张，直接跳到第五张
                index = 5;
                targetLeft = -1000*(imgCount-1);
            }else{
                index--;
                targetLeft = -1000*(index-1);
            }

        }
        $('.list').animate({left:targetLeft+'px'});




    }
    function nextPage2(next){
        var targetLeft = 0;

        if(next){//往后走
            if(index2 == 5){//到最后一张，直接跳到第一张
                targetLeft = 0;
                index2 = 1;
            }else{
                index2++;
                targetLeft = -1000*(index2-1);
            }

        }else{//往前走
            if(index2 == 1){//在第一张，直接跳到第五张
                index2 = 5;
                targetLeft = -1000*(imgCount-1);
            }else{
                index2--;
                targetLeft = -1000*(index2-1);
            }

        }
        $('.list_a').animate({left:targetLeft+'px'});




    }


</script>






